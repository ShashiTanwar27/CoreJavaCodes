package BasicCodes;

public class ShiftOperators
{
	public static void main(String... g)
	{
		int a = 18 , b=-18 , c=18;
		System.out.println("Right -  a >> 2 : "+ (a>>2));
		System.out.println("Left -  a << 2 : "+ (a<<2));
		
		
		System.out.println("Right -  b >> 2 : "+ (b>>2));
		System.out.println("Left -  b << 2 : "+ (b<<2));
		
		
		System.out.println("Unsigned Right -  c >>> 2 : "+ (c>>>2));
		//System.out.println("Unsigned Left -  c <<< 2 : "+ (c<<<3));
		
		
	}

}
